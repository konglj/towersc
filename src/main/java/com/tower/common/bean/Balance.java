package com.tower.common.bean;

public class Balance {

	private int id;
	
	private String wxid;
	
	private int userid;
	
	private int balancecount;
	
	private String balancereason;
	
	private String balancetime;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getWxid() {
		return wxid;
	}

	public void setWxid(String wxid) {
		this.wxid = wxid;
	}

	public int getUserid() {
		return userid;
	}

	public void setUserid(int userid) {
		this.userid = userid;
	}

	public int getBalancecount() {
		return balancecount;
	}

	public void setBalancecount(int balancecount) {
		this.balancecount = balancecount;
	}

	public String getBalancereason() {
		return balancereason;
	}

	public void setBalancereason(String balancereason) {
		this.balancereason = balancereason;
	}

	public String getBalancetime() {
		return balancetime;
	}

	public void setBalancetime(String balancetime) {
		this.balancetime = balancetime;
	}

	
	
	
}
